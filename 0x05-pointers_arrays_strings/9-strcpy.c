/**
 * _strcpy - function
 * Description: copies a string from one location to another
 * @src: location where tring is to be copied from
 * @dest: location string is to be copied to
 * Return: Nothing
 */
char *_strcpy(char *dest, char *src)
{
	strcpy(dest, src);
	return (dest);
}
